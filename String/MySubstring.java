package Demo;

public class MySubstring {
    
	   public static void main(String[] args)
	    {
	        String s1="Java was developed by James Gosling";
	        String s2="Java was introduced in the year of 1995";
	        String s3="Python was developed by Guido van Rossum";
	        
	        System.out.println(s1.substring(22,35));
	        System.out.println(s2.substring(35,39));
	        System.out.println(s3.substring(24,40));
	    }
}
